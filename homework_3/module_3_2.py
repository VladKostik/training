# У вас есть цыфра 2 в качестве перменной. Умножте ее на 2 двумя способами.
# Соответственно нужно ее и поделить 2 разными способами на 2.

number = 2

# First multiplication
m_1 = number * 2
print(m_1)

# Second multiplication
m_2 = number << 1
print(m_2)

# First division
d_1 = number / 2
print(d_1)

# Second division
d_2 = number >> 1
print(d_2)

